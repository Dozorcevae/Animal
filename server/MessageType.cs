namespace Animals.Networking
{
    // Команды, которыми обмениваются сервер и клиенты.
    public enum MessageType
    {
        Sync,       // клиент ➜ сервер: отправляет всю коллекцию
        Exchange,   // клиент ➜ сервер: запрос «обменять всех Type»
        Update      // сервер ➜ клиент: новая коллекция после обмена
    }
}
